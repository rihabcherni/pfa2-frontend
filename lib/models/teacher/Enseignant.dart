class Enseignant {
  final int id;
  final String cin;
  final String nom;
  final String prenom;
  final String email;
  final int age;
  final String pwd;
  final String numTel;
  final String adresse;
  final String specialite;

  Enseignant({
    required this.id,
    required this.cin,
    required this.nom,
    required this.prenom,
    required this.email,
    required this.age,
    required this.pwd,
    required this.numTel,
    required this.adresse,
    required this.specialite,
  });
}
